// Ensure provider defined types fully satisfy framework interfaces.
var _ resource.Resource = &StorageS3Resource{}
var _ resource.ResourceWithImportState = &StorageS3Resource{}

func NewStorageS3Resource() resource.Resource {
	return &StorageS3Resource{}
}

